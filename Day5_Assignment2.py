# program to declare a square() function with one parameter then call the function and pass one number and display the square of that number
#Declare square function and parameter
def square(num):
    return num*num
#call the function
number= 4
#display the result
result= square(number)
print(f"square of {number} is: {result}")

''' OUTPUT:
 square of 4 is: 16 '''

